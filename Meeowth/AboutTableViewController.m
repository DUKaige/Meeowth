#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return 2;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"cell" forIndexPath:indexPath];
    if (indexPath.row == 0)
    {
        cell.textLabel.text = @"send emails";
        cell.imageView.image = [UIImage imageNamed:@"email"];
        
    }
    else if (indexPath.row == 1)
    {
        cell.textLabel.text = @"web page";
        cell.imageView.image = [UIImage imageNamed:@"webPage"];
    }
    
    return cell;
}

-(void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    if (indexPath.row == 0)
    {
        [self businessContactWithMail];
    }
}

- (void)businessContactWithMail
{
    Class mailClass = (NSClassFromString(@"MFMailComposeViewController"));
    if (mailClass != nil)
    {
        // We must always check whether the current device is configured for sending emails
        if ([mailClass canSendMail])
        {
            MFMailComposeViewController *picker = [[MFMailComposeViewController alloc] init];
            picker.mailComposeDelegate = self;
            [picker setToRecipients:[NSArray arrayWithObject:@"meeowthofficial@126.com"]];
            [picker setSubject:[NSString stringWithFormat:@""]];
            
            
            NSString *content=[NSString stringWithFormat:@""];
            [picker setMessageBody:content isHTML:NO];
            [self presentViewController:picker animated:YES completion:^{
                
            }];
        }
    }
}
- (void)mailComposeController:(MFMailComposeViewController*)controller didFinishWithResult:(MFMailComposeResult)result error:(NSError*)error
{
    [self dismissViewControllerAnimated:YES completion:nil];
}
- (IBAction)returnToFirstPage:(id)sender {
    [self dismissViewControllerAnimated:YES completion:^{}];
}

